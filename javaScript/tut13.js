// Switch statement

let rating = 1;

switch (rating) {
  case 1: {
    console.log("rating is 1");
    break;
  }
  case 2: {
    console.log("rating 2");
    break;
  }
  case 3: {
    console.log("rating 3");
    break;
  }
  case 4: {
    console.log("rating 4");
    break;
  }
  case 5: {
    console.log("rating 5");
    break;
  }
  default:
    console.log("this is out of case");
}
